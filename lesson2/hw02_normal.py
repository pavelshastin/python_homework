# Задача-3: Напишите алгоритм, заполняющий список произвольными целыми числами
# в диапазоне от -100 до 100. В списке должно быть n - элементов.
# Подсказка:
# для получения случайного числа используйте функцию randint() модуля random

import random

min, max, n = list(map(int, input("Введите мин и макс значения случайной последовательности и количество элементов через пробел: ").split() ))

def getRandomList(min, max, n):
    randList = []

    for i in range(n):
        randList.append(random.randint(min, max))

    return randList

print(getRandomList(min, max, n))


# Задача-4: Дан список, заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут:
# а) неповторяющиеся элементы исходного списка:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 2, 4, 5, 6]
# б) элементы исходного списка, которые не имеют повторений:
# например, lst = [1 , 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 4, 6]

initList = list(map(int, input("Задача 4. Введите список значений через запятую: ").split(",")))


def getUnrptdList(initList):
    return list(set(initList))

def getUnrptdElems(initList):
    unrptdElems = []

    unrptdList = list(set(initList))

    for elem in unrptdList:
        if initList.count(elem) == 1:
            unrptdElems.append(elem)

    return unrptdElems


print(getUnrptdList(initList))
