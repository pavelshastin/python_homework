# Задача-1:
# Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

import math

initList = list(map(int, input("Задача 1. Введите список значений через запятую: ").split(",")))

def filterList(listInt):
    if type(listInt) is not list:
        return "В качестве аргумента функции должен быть передан список. Передан {}".format(type(listInt))

    resList = []
    for i in listInt:
        if i < 0:
            print(i)
            continue

        sq = math.sqrt(i)

        if str(sq).split(".")[1] != "0":
            continue

        resList.append(int(sq))

    return resList


print(initList, filterList(initList))

